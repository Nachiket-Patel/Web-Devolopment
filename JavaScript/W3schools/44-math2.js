//pow() -  returns the value of first to the power of second argument.
console.log("Power");
console.log(Math.pow(8,2));

//sqr() - returns the square root of number.
console.log("square root");
console.log(Math.sqrt(64));

//abs() - returns absolute (positive) value of number.
console.log("absolute value");
console.log(Math.abs(-8.0));

//min() - return lowest value.
//max() - return highest value.
console.log("Maximum-Minimum");
console.log(Math.min(0, 150, 30, 20, -8, -200));
console.log(Math.max(0, 150, 30, 20, -8, -200));

//sin(), cos() - return -1 to 1 of angle.
console.log("sin-cos");
console.log(Math.sin(90));
console.log(Math.cos(45));

//random() - returns a random number between 0 (inclusive), and 1 (exclusive)
console.log("random");
console.log(Math.random());

//log() - returns the natural logarithm of number.
console.log("logarithm");
console.log(Math.log(1));
//log2() - returns base 2 logarithm of number.
console.log(Math.log2(64));
//log10 - return base 10 log of number.
console.log(Math.log10(100000));
